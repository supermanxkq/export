package com.xukaiqiang.entity.common;

public class Common {
	private Payload Payload;
	private ApplySub ApplySub;
	private PaymentMethod PaymentMethod;
	private AccountList AccountList;
	private FeeList FeeList;
	private MaterialList MaterialList;
	private LoanApplyContract LoanApplyContract;

	public Payload getPayload() {
		return Payload;
	}

	public void setPayload(Payload payload) {
		Payload = payload;
	}

	public ApplySub getApplySub() {
		return ApplySub;
	}

	public void setApplySub(ApplySub applySub) {
		ApplySub = applySub;
	}

	public PaymentMethod getPaymentMethod() {
		return PaymentMethod;
	}

	public void setPaymentMethod(PaymentMethod paymentMethod) {
		PaymentMethod = paymentMethod;
	}

	public AccountList getAccountList() {
		return AccountList;
	}

	public void setAccountList(AccountList accountList) {
		AccountList = accountList;
	}

	public FeeList getFeeList() {
		return FeeList;
	}

	public void setFeeList(FeeList feeList) {
		FeeList = feeList;
	}

	public MaterialList getMaterialList() {
		return MaterialList;
	}

	public void setMaterialList(MaterialList materialList) {
		MaterialList = materialList;
	}

	public LoanApplyContract getLoanApplyContract() {
		return LoanApplyContract;
	}

	public void setLoanApplyContract(LoanApplyContract loanApplyContract) {
		LoanApplyContract = loanApplyContract;
	}

}
